package com.module1.basics;

public class ConditionalOperator {
//16-07-2025
	public static void main(String[] args) {
		//condition ? operand 1 : op2 ;
		//if the condition is true, the operand 1 will be the answer
		//if the condition is false, the operand 2 will be the answer
		int a=2;
		String result= a%2==0 ? "Even" : "Odd";
		System.out.println(result);// finding odd or even using conditional operator
		
		
		
		

	}

}
